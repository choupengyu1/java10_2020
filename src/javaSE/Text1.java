package javaSE;

public class Text1 {

	public static void main(String[] args) {
		//声明变量
		int a;
		//赋值
		a = 10;
		System.out.println(a);//必须赋值后打印
		
		//声明变量直接赋值
		int b = 20;
		System.out.println(b);
		b = 500;
		System.out.println(b);
		
		//声明小数
		double x = 12.12;
		System.out.println(x);
		//声明字符串类型
		String str = "haha";
		System.out.println(str);
		
		//设置常量
		final double pi = 3.14;
		
	}

}
